import ipaddress
import subprocess
from rich.console import Console
from bic.core import BIC_DB

console = Console()

BIRD_EXPORT_FILE_V4 = "/etc/bird/bic_export_prefixes_v4.conf"
BIRD_EXPORT_FILE_V6 = "/etc/bird/bic_export_prefixes_v6.conf"

def update_bird_export_config(db_core: BIC_DB):
    """Generates BIRD config files for IPv4 and IPv6 public prefixes."""
    console.print("\n[cyan]Updating BIRD export configuration...[/cyan]")
    try:
        pools = db_core.find_all("ip_pools")
        v4_cidrs = []
        v6_cidrs = []

        for pool in pools:
            try:
                net = ipaddress.ip_network(pool['cidr'])
                if not net.is_private:
                    if net.version == 4:
                        v4_cidrs.append(net)
                    else:
                        v6_cidrs.append(net)
            except ValueError:
                continue # Ignore invalid CIDRs in the database

        # Generate and write IPv4 config
        _write_bird_config(BIRD_EXPORT_FILE_V4, v4_cidrs, 4)

        # Generate and write IPv6 config
        _write_bird_config(BIRD_EXPORT_FILE_V6, v6_cidrs, 6)

        # Reload BIRD to apply changes
        console.print("   -> Reloading BIRD daemon...")
        subprocess.run(["sudo", "birdc", "configure"], check=True, capture_output=True, text=True)
        console.print("[green]✅ BIRD configuration updated and reloaded successfully.[/green]")

    except FileNotFoundError:
        console.print(f"[bold red]Error: 'sudo' command not found. Cannot update BIRD.[/bold red]")
    except subprocess.CalledProcessError as e:
        console.print(f"[bold red]Error reloading BIRD:[/bold red]")
        console.print(f"[red]STDOUT: {e.stdout}[/red]")
        console.print(f"[red]STDERR: {e.stderr}[/red]")
    except Exception as e:
        console.print(f"[bold red]An unexpected error occurred: {e}[/bold red]")

def _write_bird_config(filename, cidrs, version):
    content = f"# This file is automatically generated by the BIC IPAM system for IPv{version}.\n"
    content += "# Do not edit manually.\n\n"
    content += f"define BIC_EXPORT_PREFIXES_V{version} = [\n"
    if cidrs:
        content += ",\n".join([f"    {str(c)}" for c in cidrs])
    content += "\n];\n"

    temp_file_path = f"/tmp/bic_export_v{version}.tmp"
    with open(temp_file_path, "w") as f:
        f.write(content)
    
    subprocess.run(["sudo", "mv", temp_file_path, filename], check=True)
    subprocess.run(["sudo", "chown", "bird:bird", filename], check=True)

def get_next_available_ip_in_pool(db_core: BIC_DB, pool_id: int):
    pool = db_core.find_one_by('ip_pools', {'id': pool_id})
    if not pool:
        return None, None

    network = ipaddress.ip_network(pool['cidr'])
    allocated_ips_rows = db_core.find_all_by('ip_allocations', {'pool_id': pool_id})
    allocated_ips = {ipaddress.ip_address(row['ip_address']) for row in allocated_ips_rows}

    for ip in network.hosts():
        if ip not in allocated_ips:
            ip_str = str(ip)
            alloc_id = db_core.insert('ip_allocations', {'pool_id': pool_id, 'ip_address': ip_str})
            return ip_str, alloc_id
    return None, None

def add_pool(db_core: BIC_DB, name: str, cidr: str, description: str):
    """Adds a new IP pool to the database after validation."""
    try:
        net = ipaddress.ip_network(cidr)
        afi = f"ipv{net.version}"
    except ValueError:
        return {"success": False, "message": "Invalid CIDR notation."}

    db_core.insert('ip_pools', {
        'name': name,
        'afi': afi,
        'cidr': cidr,
        'description': description
    })
    # After adding a public pool, BIRD config should be updated
    update_bird_export_config(db_core)
    return {"success": True, "message": f"IP Pool '{name}' ({cidr}) created successfully."}

def delete_pool(db_core: BIC_DB, pool_id: int):
    """Deletes an IP pool after checking for active allocations."""
    # Check for any IPs in this pool that are assigned to a client
    allocations = db_core.find_all_by('ip_allocations', {'pool_id': pool_id})
    active_allocations = [a for a in allocations if a['client_id'] is not None]

    if active_allocations:
        return {"success": False, "message": f"Cannot delete pool. It has {len(active_allocations)} active IP allocation(s)."}

    # If we are here, no IPs are actively assigned. We can proceed.
    pool = db_core.find_one_by('ip_pools', {'id': pool_id})
    pool_name = pool['name']

    # Delete all placeholder allocations for this pool
    for alloc in allocations:
        db_core.delete('ip_allocations', alloc['id'])

    # Delete the pool itself
    db_core.delete('ip_pools', pool_id)

    # After deleting a public pool, BIRD config should be updated
    update_bird_export_config(db_core)

    return {"success": True, "message": f"IP Pool '{pool_name}' and its {len(allocations)} placeholder allocation(s) have been deleted."}

def get_next_available_ip_in_pool_by_name(db_core: BIC_DB, pool_name: str):
    pool = db_core.find_one_by('ip_pools', {'name': pool_name})
    if not pool:
        return None, None
    return get_next_available_ip_in_pool(db_core, pool['id'])

def get_next_available_asn(db_core: BIC_DB):
    used_asns = {res['asn'] for res in db_core.find_all('clients') if res['asn']}
    for asn in range(64512, 65535):
        if asn not in used_asns:
            return asn
    return None

