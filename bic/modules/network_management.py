def swap_pool_prefix(db_core: BIC_DB, pool_id: int, new_cidr: str):
    """Remaps all allocated IPs within a pool to a new CIDR block."""
    try:
        new_network = ipaddress.ip_network(new_cidr)
    except ValueError:
        return {"success": False, "message": "Invalid new CIDR notation."}

    pool = db_core.find_one_by('ip_pools', {'id': pool_id})
    if not pool:
        return {"success": False, "message": "Pool not found."}

    try:
        old_network = ipaddress.ip_network(pool['cidr'])
    except ValueError:
        return {"success": False, "message": "The existing pool has an invalid CIDR, cannot perform swap."}

    if old_network.num_addresses != new_network.num_addresses:
        return {"success": False, "message": "Prefix swap is only supported between networks of the same size."}

    allocations = db_core.find_all_by('ip_allocations', {'pool_id': pool_id})
    
    console.print(f"Remapping {len(allocations)} IP(s) from {old_network} to {new_network}...")
    
    for alloc in allocations:
        old_ip = ipaddress.ip_address(alloc['ip_address'])
        # Calculate the host's position in the old network
        # This is a simplification; direct integer arithmetic on IPs is better
        offset = int(old_ip) - int(old_network.network_address)
        new_ip = new_network.network_address + offset

        db_core.update('ip_allocations', alloc['id'], {'ip_address': str(new_ip)})
        console.print(f"  -> {old_ip} -> {new_ip}")

    # Finally, update the pool's CIDR
    db_core.update('ip_pools', pool_id, {'cidr': new_cidr})
    
    # After swapping a public pool, BIRD config should be updated
    update_bird_export_config(db_core)

    return {"success": True, "message": f"Successfully swapped prefix for pool '{pool['name']}'."}


def find_and_allocate_subnet(db_core: BIC_DB, pool_id: int, prefix_length: int):
    pool = db_core.find_one_by('ip_pools', {'id': pool_id})
    if not pool:
        return None, None

    try:
        parent_network = ipaddress.ip_network(pool['cidr'])
    except ValueError:
        return None, None # Invalid CIDR in database

    existing_allocations = db_core.find_all_by('ip_allocations', {'pool_id': pool_id})
    existing_subnets = db_core.find_all_by('ip_subnets', {'pool_id': pool_id})

    allocated_ips = {ipaddress.ip_address(alloc['ip_address']) for alloc in existing_allocations}
    allocated_subnets = {ipaddress.ip_network(sub['subnet']) for sub in existing_subnets}

    for potential_subnet in parent_network.subnets(new_prefix=prefix_length):
        is_available = True
        if any(potential_subnet.overlaps(existing) for existing in allocated_subnets):
            is_available = False
            continue

        for ip in allocated_ips:
            if ip in potential_subnet:
                is_available = False
                break
        
        if is_available:
            subnet_str = str(potential_subnet)
            description = f"Allocated from {pool['name']} for prefix length {prefix_length}"
            subnet_id = db_core.insert('ip_subnets', {
                'pool_id': pool_id,
                'subnet': subnet_str,
                'description': description
            })
            return subnet_str, subnet_id

    return None, None